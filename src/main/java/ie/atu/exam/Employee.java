package ie.atu.exam;

public class Employee {
    String name;
    int ppsn;
    long phone;
    String gender;
    String empType;
    int age;

    public Employee(String name, int ppsn, long phone, String gender, String empType, int age){
        this.name = name;
        this.ppsn = ppsn;
        this.phone = phone;
        this.gender = gender;
        this.empType = empType;
        this.age = age;
    }


}
